"""
charts.py

This module generates benchmark visualization charts using matplotlib.

Currently implemented:
    - Average Response Time comparison

The generated figures are exported in both PNG and PDF formats
for inclusion in the thesis.
"""

from pathlib import Path

import matplotlib.pyplot as plt
import pandas as pd


class BenchmarkCharts:
    """
    Generates visualization charts from benchmark summary statistics.
    """

    def __init__(self, dataframe: pd.DataFrame):
        """
        Initialize the chart generator.

        Args:
            dataframe: Summary dataframe generated by BenchmarkStatistics.
        """
        self.df = dataframe

    def generate_all(self):
        """
        Generate charts for every benchmark scenario.
        """

        scenarios = self.df["scenario"].unique()

        for scenario in scenarios:
            self.average_latency_chart(scenario)

    def average_latency_chart(self, scenario: str):
        """
        Generate Average Response Time chart for one benchmark scenario.

        Args:
            scenario: Benchmark scenario name.
        """

        scenario_df = self.df[self.df["scenario"] == scenario]

        mono = scenario_df[scenario_df["architecture"] == "mono"]
        micro = scenario_df[scenario_df["architecture"] == "micro"]

        plt.figure(figsize=(9, 6))

        plt.plot(
            mono["vus"],
            mono["avg_latency"],
            marker="o",
            linestyle="-",
            linewidth=2,
            label="Monolithic",
        )

        plt.plot(
            micro["vus"],
            micro["avg_latency"],
            marker="s",
            linestyle="-",
            linewidth=2,
            label="Microservices",
        )

        plt.title(
            f"{scenario.replace('-', ' ').title()} - Average Response Time",
            fontsize=18,
            fontweight="bold",
        )

        plt.xlabel(
            "Concurrent Virtual Users",
            fontsize=14,
        )

        plt.ylabel(
            "Average Response Time (ms)",
            fontsize=14,
        )

        plt.xticks(fontsize=12)

        plt.yticks(fontsize=12)

        plt.xticks([10, 50, 100, 200])

        plt.grid(
            linestyle="--",
            linewidth=0.7,
            alpha=0.7,
        )

        plt.legend(
            fontsize=12,
            loc="upper left",
        )


        output_dir = Path("outputs/charts") / scenario
        output_dir.mkdir(parents=True, exist_ok=True)

        plt.tight_layout(pad=2)

        plt.savefig(output_dir / "avg_latency.png", dpi=300)

        plt.savefig(output_dir / "avg_latency.pdf")

        plt.close()