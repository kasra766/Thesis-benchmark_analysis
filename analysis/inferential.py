"""
inferential.py

This module performs inferential statistical analysis on benchmark
results. It compares the performance of the monolithic and
microservices architectures for every benchmark scenario and
concurrency level.

Computed statistics include:

    - Independent two-sample t-test
    - Cohen's d effect size
    - 95% confidence interval of the mean
"""

from typing import List

import numpy as np
import pandas as pd
from scipy import stats


class InferentialStatistics:
    """
    Performs hypothesis testing and computes inferential statistics.
    """

    def __init__(self, dataframe: pd.DataFrame):
        """
        Initialize the analysis.

        Args:
            dataframe: Raw benchmark dataframe generated by BenchmarkParser.
        """
        self.df = dataframe

    def analyze(self) -> pd.DataFrame:
        """
        Compare monolithic and microservices results for every
        scenario and VU level.

        Returns:
            DataFrame containing inferential statistics.
        """

        rows = []

        scenarios = sorted(self.df["scenario"].unique())
        vus_values = sorted(self.df["vus"].unique())

        for scenario in scenarios:
            for vus in vus_values:

                subset = self.df[
                    (self.df["scenario"] == scenario)
                    & (self.df["vus"] == vus)
                ]

                mono = subset[
                    subset["architecture"] == "mono"
                ]["avg_ms"].values

                micro = subset[
                    subset["architecture"] == "micro"
                ]["avg_ms"].values

                t_stat, p_value = stats.ttest_ind(
                    mono,
                    micro,
                    equal_var=False,
                )


                mono_mean = np.mean(mono)
                micro_mean = np.mean(micro)

                mono_std = np.std(mono, ddof=1)
                micro_std = np.std(micro, ddof=1)

                cohen_d = self._cohens_d(mono, micro)

                rows.append(
                    {
                        "scenario": scenario,
                        "vus": vus,

                        "mono_mean": mono_mean,
                        "micro_mean": micro_mean,

                        "mono_std": mono_std,
                        "micro_std": micro_std,

                        "mono_ci95": self._confidence_interval(mono),
                        "micro_ci95": self._confidence_interval(micro),

                        "t_statistic": t_stat,

                        "p_value": self._format_p_value(p_value),

                        "significant": self._is_significant(p_value),

                        "cohen_d": round(abs(cohen_d), 2),

                        "effect_size": self._effect_size(cohen_d),

                        "Lower Latency": self._winner(
                            mono_mean,
                            micro_mean,
                            p_value,
                        ),
                    }
                )

        result = pd.DataFrame(rows)

        numeric_columns = [
            "mono_mean",
            "micro_mean",
            "mono_std",
            "micro_std",
            "mono_ci95",
            "micro_ci95",
            "t_statistic",
        ]

        result[numeric_columns] = result[numeric_columns].round(4)

        return result

    def _confidence_interval(
        self,
        values: List[float],
        confidence: float = 0.95,
    ) -> float:
        """
        Compute half-width of the confidence interval.

        Args:
            values: Sample values.
            confidence: Confidence level.

        Returns:
            Confidence interval half-width.
        """

        values = np.asarray(values)

        sem = stats.sem(values)

        return sem * stats.t.ppf(
            (1 + confidence) / 2,
            len(values) - 1,
        )

    def _cohens_d(
        self,
        group1: List[float],
        group2: List[float],
    ) -> float:
        """
        Compute Cohen's d effect size.

        Args:
            group1: First sample.
            group2: Second sample.

        Returns:
            Cohen's d.
        """

        group1 = np.asarray(group1)
        group2 = np.asarray(group2)

        pooled_std = np.sqrt(
            (
                ((len(group1) - 1) * np.var(group1, ddof=1))
                + ((len(group2) - 1) * np.var(group2, ddof=1))
            )
            / (len(group1) + len(group2) - 2)
        )

        return (np.mean(group1) - np.mean(group2)) / pooled_std

    def _format_p_value(self, p_value: float) -> str:
        """
        Format a p-value for presentation.

        Args:
            p_value: Raw p-value.

        Returns:
            Formatted p-value string.
        """

        if p_value < 0.001:
            return "< 0.001"

        return f"{p_value:.3f}"

    def _is_significant(self, p_value: float) -> str:
        """
        Determine whether the result is statistically significant.

        Args:
            p_value: Raw p-value.

        Returns:
            "Yes" if p < 0.05, otherwise "No".
        """

        return "Yes" if p_value < 0.05 else "No"

    def _effect_size(self, d: float) -> str:
        """
        Interpret Cohen's d.

        Args:
            d: Cohen's d value.

        Returns:
            Effect size interpretation.
        """

        d = abs(d)

        if d < 0.2:
            return "Negligible"

        if d < 0.5:
            return "Small"

        if d < 0.8:
            return "Medium"

        return "Large"

    def _winner(
            self,
            mono_mean: float,
            micro_mean: float,
            p_value: float,
    ) -> str:
        """
        Determine the better-performing architecture.

        A winner is declared only when the difference between
        architectures is statistically significant (p < 0.05).

        Args:
            mono_mean: Mean latency of the monolithic architecture.
            micro_mean: Mean latency of the microservices architecture.
            p_value: Statistical test p-value.

        Returns:
            Winner label.
        """

        if p_value >= 0.05:
            return "No Significant Difference"

        if mono_mean < micro_mean:
            return "Monolithic"

        return "Microservices"