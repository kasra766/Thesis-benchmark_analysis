"""
parser.py

This module is responsible for reading all k6 benchmark result files,
extracting the required performance metrics, and converting them into
a single pandas DataFrame for further statistical analysis.
"""

from pathlib import Path
import json

import pandas as pd


class BenchmarkParser:
    """
    Reads benchmark JSON files generated by k6 and converts them into
    a structured pandas DataFrame.
    """

    def __init__(self, benchmark_root: str):
        """
        Initialize the parser.

        Args:
            benchmark_root: Root directory containing benchmark results.
        """
        self.root = Path(benchmark_root)

    def parse(self) -> pd.DataFrame:
        """
        Parse every JSON benchmark file.

        Returns:
            DataFrame containing one row per benchmark execution.
        """
        rows = []

        # Search recursively for every JSON file
        for json_file in self.root.rglob("*.json"):
            rows.append(self._parse_file(json_file))

        return pd.DataFrame(rows)

    def _parse_file(self, file_path: Path) -> dict:
        """
        Parse a single benchmark result.

        Args:
            file_path: Path to one k6 JSON summary.

        Returns:
            Dictionary containing extracted benchmark metrics.
        """

        with open(file_path, "r") as f:
            data = json.load(f)

        # Folder structure:
        # benchmarks/mono/login/login_10_run1.json
        architecture = file_path.parts[-3]
        scenario = file_path.parts[-2]

        # Extract VUs and run number from filename
        filename = file_path.stem
        parts = filename.split("_")

        vus = int(parts[-2])
        run = int(parts[-1].replace("run", ""))

        metrics = data["metrics"]

        return {
            "architecture": architecture,
            "scenario": scenario,
            "vus": vus,
            "run": run,

            # Response time metrics (milliseconds)
            "avg_ms": metrics["http_req_duration"]["avg"],
            "med_ms": metrics["http_req_duration"]["med"],
            "p90_ms": metrics["http_req_duration"]["p(90)"],
            "p95_ms": metrics["http_req_duration"]["p(95)"],
            "max_ms": metrics["http_req_duration"]["max"],

            # Throughput metrics
            "req_per_sec": metrics["http_reqs"]["rate"],
            "iter_per_sec": metrics["iterations"]["rate"],

            # Failure rate
            "error_rate": (
                metrics["checks"]["fails"] / metrics["checks"]["passes"]
                if metrics["checks"]["passes"] > 0
                else 0
            ),
        }